public class DataTypes {
    public static void main(String args[]) {
        byte num1 = 127;
        short num2 = 32000;
        int num3 = 100000;
        float num4 = 5.20f;
        double num5 = 20.6596352635;
        char ch = 'a';
        boolean bool = true;
        String msg = "Hello World!";
        System.out.println("\n");
        System.out.println("Primitive Data Types");
        System.out.println("Byte value: " + num1);
        System.out.println("Short Value: " + num2);
        System.out.println("Int Value: " + num3);
        System.out.println("Float Value: " + num4);
        System.out.println("Double Value: " + num5);
        System.out.println("Character value: " + ch);
        System.out.println("Boolean Value: " + bool);
        System.out.println("\n");
        System.out.println("Non Primitive Data Types");
        System.out.println("String Value: " + msg);
    }
}
